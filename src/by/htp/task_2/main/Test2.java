package by.htp.task_2.main;

public class Test2 {

	private int x;
	private int y;
	
	public Test2() {
		this.x = 0;
		this.y = 0;
	}
	public Test2(int x, int y) {
		this.x = x;
		this.y = y;
	}
	
	public void setX(int nx) {
		this.x = nx;
	}
	public void setY(int ny) {
		this.y = ny;
	}
	public int getX() {
		return this.x;
	}
	public int getY() {
		return this.y;
	}

}
